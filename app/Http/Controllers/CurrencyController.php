     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $apiCall = new CurrencyService();
        $data['INR'] = 'INR';
        $result = $apiCall->call('GET', '/latest', $data);
        if(!empty(json_decode($result)->rates)){
            $rates = json_decode($result)->rates;
            return array(['rates' => $rates,'message' => 'API success']);
        }
        return array(['message' => 'API Error','rates' => '']); 
    }

